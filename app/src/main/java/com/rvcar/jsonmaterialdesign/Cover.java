package com.rvcar.jsonmaterialdesign;


public class Cover {

    private String small = null;
    private String big = null;

    public Cover() {

    }

    public void setSmall(String sUrl) {
        small = sUrl;
    }

    public void setBig(String bUrl) {
        big = bUrl;
    }

    public String getSmall() {
        return small;
    }

    public String getBig() {
        return big;
    }
}
